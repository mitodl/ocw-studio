def _scan_feature_flags(prefix: str = "FEATURE_") -> dict[str, bool]:
    """Scan ``os.environ`` for ``FEATURE_``-prefixed variables.

    Mirrors ``mitol.common.envs.EnvParser.get_features()``: every environment
    variable starting with *prefix* becomes a feature flag, keyed by the
    remainder of its name with the prefix stripped, parsed as a boolean.

    Args:
        prefix: The environment variable prefix that marks a feature flag.

    Returns:
        A dict of feature name to boolean value.

    Raises:
        ImproperlyConfigured: If a ``FEATURE_*`` variable isn't ``"true"`` or
            ``"false"`` (case-insensitive).
    """
    features: dict[str, bool] = {}
    for key, value in os.environ.items():
        if not key.startswith(prefix):
            continue
        parsed = value.strip().lower()
        if parsed == "true":
            features[key[len(prefix) :]] = True
        elif parsed == "false":
            features[key[len(prefix) :]] = False
        else:
            msg = f"Expected value in {key}={value} to be a boolean"
            raise ImproperlyConfigured(msg)
    return features


