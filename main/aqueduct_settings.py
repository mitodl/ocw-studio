class DevAqueductSettings(AqueductSettings):
    """Local-dev variant: fills missing values from Vault via OIDC."""

    @classmethod
    def settings_customise_sources(
        cls,
        settings_cls,
        init_settings,
        env_settings,
        dotenv_settings,  # noqa: ARG003
        file_secret_settings,  # noqa: ARG003
    ):
        """Layer a Vault OIDC source under env vars so devs skip a .env file."""
        from django_aqueduct.sources.vault import VaultSettingsSource  # noqa: PLC0415

        # NOTE: VAULT_AQUEDUCT_PATH and the default OIDC role ("ocw-studio") are
        # placeholders. They need to be confirmed against the live Vault server
        # (e.g. `vault kv list secret-ocw-studio/`) before this is used for real;
        # nothing in this repo tells us the exact secret path or role name.
        return (
            init_settings,
            env_settings,
            VaultSettingsSource(
                settings_cls,
                vault_url=os.environ["VAULT_ADDR"],
                vault_path=os.environ.get("VAULT_AQUEDUCT_PATH", ""),
                mount_point="secret-ocw-studio",
                kv_version="2",
                auth_method="oidc",
                role=os.environ.get("VAULT_AQUEDUCT_ROLE", "ocw-studio"),
            ),
        )
