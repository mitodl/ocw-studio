"""Typed Aqueduct settings model for ocw-studio.

Generated by ``manage.py generate_aqueduct_settings`` and then hand-refined:

* Field descriptions/required-ness were reconciled between the plain module
  scan of ``main.settings`` and the mitol ``EnvParser`` registry scan
  (``--include-envparser``). Where the Django-setting attribute name differs
  from the real environment variable name (e.g. ``SITE_ID`` is populated from
  ``OCW_STUDIO_SITE_ID``), the field keeps the Django name and uses
  ``validation_alias`` to point at the real env var.
* Settings pulled into ``main.settings`` via
  ``import_settings_modules("mitol.common.settings.base", ...)`` (e.g.
  ``SITE_BASE_URL``) are included as ordinary fields.
* The three settings in ``main/envs.py`` (``OCW_TEST_SITE_SLUGS``,
  ``OCW_EXTRA_THEMES_BASE_URLS``, ``OCW_EXTRA_THEMES_GTM_IDS``) bypass the
  mitol ``EnvParser`` registry entirely, so they are declared by hand below.
* Derived/composite settings (``DATABASES``, ``INSTALLED_APPS``,
  ``MIDDLEWARE``, ``CACHES``, ``CELERY_BEAT_SCHEDULE``, etc.) and the
  cross-field validations that exist in ``main/settings.py`` today are
  reproduced with ``@model_validator`` methods below, preserving the exact
  runtime behavior of the original settings module.

This module is purely additive: ``main/settings.py`` and ``main/envs.py`` are
untouched and remain the default settings used everywhere until a settings
module built on top of this model is explicitly selected via
``DJANGO_SETTINGS_MODULE``.
"""

